package Day15WhileLoop;

import java.util.Scanner;

public class harfleriyazdir {
    public static void main(String[] args) {
        //Soru 1- ’a’ harfinden ‘l’ harfine kadar harfleri yazdirin
        char baslangic = 'a';
        char bitis = 'l';
        do {
            System.out.print((char) baslangic+" ");
            baslangic++;
        } while (baslangic <= bitis) ;



    }







}
